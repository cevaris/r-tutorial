# checks if left value is less than right value
4 < 5    # return TRUE

# checks if left value is greater than right value
10 > 100 # return FALSE

# checks if values are equal
4 == 5   # returs FALSE

# checks if numbers are not equal
4 != 5   # returns TRUE

# flip the logical value using "!" character
TRUE == !TRUE # returns FALSE
TRUE == TRUE  # returns TRUE

varTrue <- TRUE
varFalse <- FALSE

# practice using the or "|" operator
varTrue | varTrue   # returns TRUE
varFalse | varTrue  # returns TRUE
varFalse | varFalse # returns FALSE

# practice using the or "&" operator
varTrue  & varTrue  # returns TRUE
varFalse & varTrue  # returns FALSE
varFalse & varFalse # returns FALSE

